double myPow(double x, int n) {

    if (x == 0 || x == 1 || n == 1)
        return x;
    if (x == -1)
        return n % 2 == 0 ? 1 : -1;
    if (n == 0)
        return 1;
    if (n == -1)
        return 1 / x;

    bool neg = false;

    if (n < 0) {
        n = -n;
        neg = true;
    }

    unsigned i, l = ceil(log2(n)) + 1;
    long double result = 1;
    long double ms[l];
    for (i = 0; i < l; i++) {
        if (i == 0)
            ms[i] = x;
        else
            ms[i] = ms[i - 1] * ms[i - 1];
        if (n & (1 << i))
            result *= ms[i];
    }

    return neg ? 1 / result : result;
}

